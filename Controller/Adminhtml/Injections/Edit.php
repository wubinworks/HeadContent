declare(strict_types=1);

namespace Wubinworks\InjectHead\Controller\Adminhtml\Injections;

class Edit extends \Wubinworks\InjectHead\Controller\Adminhtml\Injections
{
	/**
	 * Result page factory
	 * @var \Magento\Framework\View\Result\PageFactory $resultPageFactory
	 */
    protected $resultPageFactory;

    /**
     * @param \Magento\Backend\App\Action\Context $context
     * @param \Magento\Framework\Registry $coreRegistry
     * @param \Magento\Framework\View\Result\PageFactory $resultPageFactory
     */
    public function __construct(
        \Magento\Backend\App\Action\Context $context,
        \Magento\Framework\Registry $coreRegistry,
        \Magento\Framework\View\Result\PageFactory $resultPageFactory
    ) {
        $this->resultPageFactory = $resultPageFactory;
        parent::__construct($context, $coreRegistry);
    }

    /**
     * Edit action
     *
     * @return \Magento\Framework\Controller\ResultInterface
     */
    public function execute()
    {
        // 1. Get ID and create model
        $id = $this->getRequest()->getParam('injections_id');
        $model = $this->_objectManager->create(\Wubinworks\InjectHead\Model\Injections::class);

        // 2. Initial checking
        if ($id) {
            $model->load($id);
            if (!$model->getId()) {
                $this->messageManager->addErrorMessage(__('This Rule no longer exists'));
                /** @var \Magento\Backend\Model\View\Result\Redirect $resultRedirect */
                $resultRedirect = $this->resultRedirectFactory->create();
                return $resultRedirect->setPath('*/*/');
            }
        }
        $this->_coreRegistry->register('wubinworks_injecthead_injections', $model);

        // 3. Build edit form
        /** @var \Magento\Backend\Model\View\Result\Page $resultPage */
        $resultPage = $this->resultPageFactory->create();
		$this->navArr[] = __('Head Content Manager');
		$this->navArr[] = $model->getId() ? __('Edit Rule') . __(' ID: ') . $model->getId() : __('New Rule');
        $this->initPage($resultPage);
        return $resultPage;
    }
}
